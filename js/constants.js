if (!CanvasRenderingContext2D.prototype.ellipse) {
  CanvasRenderingContext2D.prototype.ellipse = function(cx, cy, rx, ry, rot, start, end, ccw) {
    this.save();
    this.translate(cx, cy);
    this.rotate(rot);
    this.scale(rx, ry);
    this.arc(0, 0, 1, start, end, ccw);
    this.restore();
  };
}

